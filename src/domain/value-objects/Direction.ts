// Value Object: Dirección de movimiento en el juego Arrow Maze.
// Este enum es puro y no depende de ningún framework externo.
export enum Direction {
  // El jugador o la celda apunta hacia arriba.
  UP = 'UP',
  // El jugador o la celda apunta hacia abajo.
  DOWN = 'DOWN',
  // El jugador o la celda apunta hacia la izquierda.
  LEFT = 'LEFT',
  // El jugador o la celda apunta hacia la derecha.
  RIGHT = 'RIGHT',
}
